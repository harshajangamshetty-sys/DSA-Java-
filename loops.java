public class loops {
    public static void main(String[] args) {
       for(int i = 0;i<11;i++){
        System.out.println(i);
       }

    }
    
}
class Innerloops {

    public static void main(String[] args) {
        int i = 0;
        while (i < 11) {
            System.out.println(i);
            i++;

            
        }
    }
}
class Innerloops_1 {

    public static void main(String[] args) {
        int i = 0;
        do {
            System.out.println(i);
            i++;
        } while (i < 11);
    }
}
